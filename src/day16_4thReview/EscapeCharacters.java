package day16_4thReview;

public class EscapeCharacters {
    public static void main(String[] args) {
        // Escape Char
        //   \t      \*     \n     dounle back slash (\\)
        System.out.println("\"");   //back slash(\) can print out anything
        System.out.println("\tsomething");
        System.out.println("name :\t\tLeng");
        System.out.println("occupancy :\tsdet");
        System.out.println("myString\nenter new line");

        String filePath = "c:\\myComputer\\user\\documents";
        System.out.println(filePath);

        char symbol = '\'';
        System.out.println(symbol);
    }

}
